'''
    인덱스(index)
        1. 각 문자마다 부여된 번호
        2. 맨 앞에서부터 0으로 번호가 시작된다.
        3. 맨 뒤에서부터는 -1로 번호가 시작된다.
'''
s = 'Hello'
print(s[0],s[-5]) # H
print(s[1],s[-4]) # e
print(s[2],s[-3]) # l
print(s[3],s[-2]) # l
print(s[4],s[-1]) # o
